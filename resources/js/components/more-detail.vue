<template lang="pug">
div
  div(v-if="description.length > 0")
    p.small(v-if="clicked")
      span {{ description }}
    p
      a(@click="toggle()")
        small.text(v-if="clicked")
          | Close details 
          i.fa.fa-angle-up
        small.text(v-else)
          | Read details 
          i.fa.fa-angle-down
</template>

<script>
export default {
  name: 'MoreDetail',
  data () {
    return {
      clicked: false,
    }
  },
  methods: {
    toggle () {
      this.clicked = !this.clicked
    },
  },
  props: ['description'],
}
</script>